package kr.or.ddit.emp.hrm.empnotice.exception;

/**
 * 게시판에서 발생할 수 있는 모든 예외적 상황의 표현
 *
 */
public class EmpNoticeException extends RuntimeException{

	public EmpNoticeException() {
		super();
	}

	public EmpNoticeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public EmpNoticeException(String message, Throwable cause) {
		super(message, cause);
	}

	public EmpNoticeException(String message) {
		super(message);
	}

	public EmpNoticeException(Throwable cause) {
		super(cause);
	}

}
